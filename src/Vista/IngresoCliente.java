    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int cli_run = Integer.parseInt(jTextField1.getText());
        int dv_run = Integer.parseInt(jTextField2.getText());
        String pnombre = jTextField3.getText();
        String snombre = jTextField4.getText();
        String apaterno = jTextField5.getText();
        String amaterno = jTextField6.getText();
        String fecha_nac = jTextField7.getText();
        String direccion = jTextField8.getText();
        String correo = jTextField9.getText();
        int tel_movil = Integer.parseInt(jTextField10.getText());
        
        Cliente cli_1 = new Cliente(cli_run,dv_run,pnombre,snombre,apaterno,amaterno,fecha_nac,direccion,correo,tel_movil);
        
        ClienteController control = new ClienteController();
        int response = control.insertCliente(cli_1);
        
        if (response == 1) {
            JOptionPane.showMessageDialog(null, "El registro fue ingresado.");
            mostrar();
        } else if (response == 2) {
            JOptionPane.showMessageDialog(null, "Ya existe un cliente con este RUN.");
        } else {
            JOptionPane.showMessageDialog(null, "Hubo un error, vuelva a ingresar.");
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

