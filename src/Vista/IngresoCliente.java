    public void mostrar () {
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = ClienteController.listarClientes(""
                + "SELECT "
                + "\n CONCAT(CLI_RUN,"+ "'-'" +",CLI_DV) CLIENTE_RUT, "
                + "\n UPPER(CONCAT(PNOMBRE,"+ "' '" +", SNOMBRE ,"+ "' '" +", APATERNO ,"+ "' '" +", AMATERNO)) NOMBRE_CLIENTE, "
                + "\n TIMESTAMPDIFF(YEAR, FECHA_NAC, CURDATE()) EDAD, "
                + "\n UPPER(DIRECCION) DIRECCION, "
                + "\n UPPER(CORREO) CORREO, "
                + "\n TEL_MOVIL MOVIL "
                + "\n FROM CLIENTE");
        modelo.setColumnIdentifiers(new Object[]{"RUT CLIENTE","NOMBRE DEL CLIENTE","EDAD","DIRECCION","CORREO","MOVIL"});
        try {
            while (rs.next()) {
                modelo.addRow(new Object[]
                {rs.getString("CLIENTE_RUT"),
                 rs.getString("NOMBRE_CLIENTE"), 
                 rs.getInt("EDAD"), 
                 rs.getString("DIRECCION"),
                 rs.getString("CORREO"),
                 rs.getInt("MOVIL")});
            }
            jTable1.setModel(modelo);
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
